CHARS_ORDER: list[str] = list("ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789")


def bits_for(char: str) -> list[float]:
    """Return 25 floats (0.0 unlit, 1.0 lit), row-major: index = y*5 + x."""
    rows = FONT_5x5[char]
    return [1.0 if rows[y][x] == "#" else 0.0
            for y in range(5)
            for x in range(5)]
